#include <cstdio>
#include <iostream>
#include <cmath>
#include <climits>

using namespace std;

struct node {
	int val;
	struct node * left;
	struct node * right;
};

void inOrder(node *);
node * createNode(int);
node * getPath(int);
void showList(node *, const char *);
void showListRev(node *, const char *);
node * insert(node *, node *, node *);
node * findHead(node *);
node * findTail(node *);
node * convert(node *);

int main() {
	node * root;
	node * head, * tail;
	int val, pos;

	printf("\nEnter (val, pos) to insert into tree:-\n(pos starts from 1)\n");
	scanf("%d%d", &val, &pos);

	root = NULL;
	while (val != -1 && pos != -1) {
		node * path = getPath(pos);
		root = insert(root, path -> right, createNode(val));

		scanf("%d%d", &val, &pos);
	}

	printf("\nThe inOrder traversal is:-\n");
	inOrder(root);
	printf("\n");

	root = convert(root);

	head = findHead(root);
	showList(head, "forward linkedlist");

	tail = findTail(root);
	showListRev(tail, "reverse linkedList");

	return 0;
}

node * createNode(int val) {
	node * newNode = new node;

	newNode -> val = val;
	newNode -> left = NULL;
	newNode -> right = NULL;

	return newNode;
}

void inOrder(node * root) {
	if (root != NULL) {
		inOrder(root -> left);
		printf("%d ", root -> val);
		inOrder(root -> right);
	}
}

node * getPath(int val) {
	node * head;

	head = createNode(val);
	val /= 2;

	while (val >= 1) {
		node * newNode = createNode(val);
		newNode -> right = head;
		head = newNode;

		val /= 2;
	}

	return head;
}

void showList(node * head, const char * msg) {
	printf("\nThe %s is:-\n", msg);
	for (node * list = head; list != NULL; list = list -> right) {
		printf("%d -> ", list -> val);
	}
	printf("NULL\n");
}

void showListRev(node * head, const char * msg) {
	printf("\nThe %s is:-\n", msg);
	for (node * list = head; list != NULL; list = list -> left) {
		printf("%d -> ", list -> val);
	}
	printf("NULL\n");
}

node * insert(node * root, node * path, node * newNode) {
	if (path == NULL) {
		return newNode;
	} else {
		if (path -> val % 2 == 1) {
			root -> right = insert(root -> right, path -> right, newNode);
		} else {
			root -> left = insert(root -> left, path -> right, newNode);
		}

		return root;
	}
}

node * findHead(node * tmp) {
	while (tmp != NULL && tmp -> left != NULL) {
		tmp = tmp -> left;
	}
	return tmp;
}

node * findTail(node * tmp) {
	while (tmp != NULL && tmp -> right != NULL) {
		tmp = tmp -> right;
	}
	return tmp;
}

node * convert(node * root) {
	if (root != NULL) {
		if (root -> left != NULL) {
			node * lList = convert(root -> left);

			node * tail = findTail(lList);
			tail -> right = root;
			root -> left = tail;
		}
		if (root -> right != NULL) {
			node * rList = convert(root -> right);

			node * head = findHead(rList);
			head -> left = root;
			root -> right = head;
		}
	}
	return root;
}
